<p><b>Jmol measurements</b></p>

<ul>

  <li>
    <p> Distance (2 atoms):</p>

    <ol type="1">

      <li>
        <p>double-click on the starting atom</p>

      </li>

      <li>
        <p>to fix a distance measurement, double-click on second
atom</p>

      </li>

    </ol>

  </li>

  <li>
    <p> Angle (3 atoms):</p>

    <ol type="1">

      <li>
        <p>double-click on the starting atom</p>

      </li>

      <li>
        <p>click on the second atom (central atom in angle) </p>

      </li>

      <li>
        <p>to fix an angle measurement, double-click on third atom</p>

      </li>

    </ol>

  </li>

  <li>
    <p> Torsion angle or dihedral (4 atoms)</p>

    <ol type="1">

      <li>
        <p>double-click on the starting atom</p>

      </li>

      <li>
        <p>click on the second atom</p>

      </li>

      <li>
        <p>click on the third atom</p>

      </li>

      <li>
        <p>to fix a dihedral angle measurement, double-click on
fourth atom</p>

      </li>

    </ol>

  </li>

  <li>
    <p> In all cases:</p>

    <ul>

      <li>
        <p> move pointer over destination atom in order to see
measurement results without leaving a permanent measurement</p>

      </li>

      <li>
        <p> move outside the window in order to cancel the
measurement</p>

      </li>

      <li>
        <p> make the same measurement again in order to delete
the measurement</p>

      </li>

    </ul>

  </li>

</ul>
